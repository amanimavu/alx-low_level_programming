/**
  * add - computes the sum of 2 numbers
  * @num1: 1st int type parameter to be added
  * @num2: 2nd int type parameter
  *
  * Description: Function takes two input of type int
  * and computes their sum
  * Return: sum of 2 numbers
 */


int add(int num1, int num2)
{
	int sum;

	sum = num1 + num2;
	return (sum);
}
